import java.util.*;
import java.lang.*;


class Solution {
    /**
    primeFib returns n-th number that is a Fibonacci number and it's also prime.
    >>> primeFib(1)
    2
    >>> primeFib(2)
    3
    >>> primeFib(3)
    5
    >>> primeFib(4)
    13
    >>> primeFib(5)
    89
     */
    public int primeFib(int n) { 
        int i = 1, j = 1, count = 0;
        while (true) {
            int temp = i + j;
            i = j;
            j = temp;
            if (isPrime(j)) {
                count++;
                if (count == n) {
                    return j;
                }
            }
        }
    }
    
    public boolean isPrime(int num) {
        if (num <= 1) {
            return false;
        }
        for (int i = 2; i * i <= num; i++) {
            if (num % i == 0) {
                return false;
            }
        }
        return true;
    }
}


